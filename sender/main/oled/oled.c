static const char *TAG = "OLED";

static void oled_send_cmd(uint8_t cmd);
static void oled_send_data(uint8_t *data, size_t len);

static void oled_send_cmd(uint8_t cmd) {
    i2c_cmd_handle_t cmd_h = i2c_cmd_link_create();
    i2c_master_start(cmd_h);
    i2c_master_write_byte(cmd_h, (OLED_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(cmd_h, 0x00, true);  // Co = 0, D/C = 0
    i2c_master_write_byte(cmd_h, cmd, true);
    i2c_master_stop(cmd_h);
    i2c_master_cmd_begin(OLED_I2C_PORT, cmd_h, pdMS_TO_TICKS(100));
    i2c_cmd_link_delete(cmd_h);
}

static void oled_send_data(uint8_t *data, size_t len) {
    // gửi block dữ liệu (prefix 0x40)
    i2c_cmd_handle_t cmd_h = i2c_cmd_link_create();
    i2c_master_start(cmd_h);
    i2c_master_write_byte(cmd_h, (OLED_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(cmd_h, 0x40, true);  // Co = 0, D/C = 1
    i2c_master_write(cmd_h, data, len, true);
    i2c_master_stop(cmd_h);
    i2c_master_cmd_begin(OLED_I2C_PORT, cmd_h, pdMS_TO_TICKS(100));
    i2c_cmd_link_delete(cmd_h);
}

